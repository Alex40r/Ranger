package ranger.ui.view;

/**
 * Enum representing the direction of a selection range movement.
 */
public enum SelectionDirection {
    /**
     * The up movement direction.
     */
    UP,

    /**
     * The down movement direction.
     */
    DOWN,

    /**
     * The left movement direction.
     */
    LEFT,

    /**
     * The right movement direction.
     */
    RIGHT
}
